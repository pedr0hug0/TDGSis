            txt_min_max_peca.setText(qtd_total_max.toString());
            
            
            
            //**************max
            System.out.println("Calc Folha MAX: "+max);
        
        //MIN TOTAL FOLHAS
        Double max_folha_por_cor = max/qtd_cor;
        txt_max_folha_cor.setText(String.format("%.2f", max_folha_por_cor));
        
        
        produz = (max * qtd_folha);
        System.out.println("Max Produz total de: "+produz);
        txt_max_pecas.setText(produz.toString());
        
        
        
        
        qtd_por_cor = produz/qtd_cor;
