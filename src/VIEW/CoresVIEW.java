    private void btn_Listar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Listar1ActionPerformed
        // TODO add your handling code here:
        //listar cores cadastradas na ref
        ProdutoCoresDAO dao = new ProdutoCoresDAO();
        DefaultTableModel model =  (DefaultTableModel) jTable_cores_produto.getModel(); 
            
        model.setNumRows(0);
        for (CoresDTO cores : dao.ListarProdutoCores("%")) {
            model.addRow(new Object[]{cores.getId(), cores.getCodigo(), cores.getCor(), cores.getId_cor()});
        }
    }//GEN-LAST:event_btn_Listar1ActionPerformed
    
    private void adicionar_cor(){
        CoresDTO cores = new CoresDTO();
        
        String cor = txt_cor.getText();
        String categoria = jComboBox_categoria.getSelectedItem().toString();
        
        
        cores.setCor(cor);
        cores.setCategoria(categoria);
        
        CoresDAO dao = new CoresDAO();
        if (dao.adicionaCor(cores)){
            DefaultTableModel model =  (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{cores.getId(),cores.getCor(), cores.getCategoria()});
            txt_cor.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "ERRO AO ADICIONAR COR");
        }
    }
    
