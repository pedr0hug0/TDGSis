    //listar cortes sem infestar
     public void listar_cortes_infestos(){
        jTable1.setRowSorter(null);
        String descricao = (String) jComboBox_Descricao.getSelectedItem();
        
        String infesto = (String) jComboBox_infesto.getSelectedItem();
        
        String codigo = txt_ref_pesquisar.getText();
        if (codigo.equals("") || codigo.equals(null)){
            codigo = "%";
        }
        
        
         CorteDAO dao = new CorteDAO();
         DefaultTableModel model =  (DefaultTableModel) jTable1.getModel(); 
            
       model.setNumRows(0);
       //SimpleDateFormat formatarData = new SimpleDateFormat("dd/MM/yyyy");
        for (CorteDTO c : dao.getCorteInfesto(descricao, infesto)) {

            model.addRow(new Object[]{c.getId(), new Boolean(true), c.getCorte(), c.getCodigo(), c.getCor(), c.getQtd(), c.getT1(), c.getT2(), c.getT3(), c.getT4(), c.getInfesto(), c.getData_corte()});
        }
       //adicionar rowsorter 
       jTable1.setAutoCreateRowSorter(true);
    }//fim listar cortes infesto
    
        
