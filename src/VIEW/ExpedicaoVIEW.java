    //listar item pedido
     public void listar_item_pedido(){
        if ( (txt_n_pedido_pesquisar.getText() == null)  || (txt_n_pedido_pesquisar.getText().equals("")) ){
            JOptionPane.showMessageDialog(null, "Insira o Nº Pedido");
            txt_n_pedido_pesquisar.requestFocus();
        }else{
        
         ExpedicaoDAO dao = new ExpedicaoDAO();
         DefaultTableModel model =  (DefaultTableModel) jTable1.getModel(); 
            
       model.setNumRows(0);
       
        for (ExpedicaoDTO e : dao.getItemPedido(txt_n_pedido_pesquisar.getText())) {
           
            model.addRow(new Object[]{e.getCodigo(), e.getCor(), e.getTotal_t1(), e.getTotal_t2(), e.getTotal_t3(), e.getTotal_t4(), e.getTotal_ref()});
            txt_status.setText(e.getStatus());
        }
        
        }//fim else n_pedido vazio
    }//fim listar
    
    
