    private void btn_ExpedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExpedirActionPerformed
        // TODO add your handling code here:
        
        //Expedir = Ler dados da tabela e dar baixa no estoque com o n_pedido

        //pegando data atual 
      String data_saida = txt_data.getText();
       Integer n_pedido = Integer.parseInt(txt_n_pedido_pesquisar.getText());
       
       
       //dar baixa no estoque
        for(int i=0;i<jTable1.getRowCount();i++){ 
           //se status = expedido = desativar botao expedir.
                String ref = (String) jTable1.getValueAt(i,0);
                String cor = (String) jTable1.getValueAt(i,1);
                Integer t1 = (Integer) jTable1.getValueAt(i,2);
                Integer t2 = (Integer) jTable1.getValueAt(i,3);
                Integer t3 = (Integer) jTable1.getValueAt(i,4);
                Integer t4 = (Integer) jTable1.getValueAt(i,5);
                
                
                //transformando em negativo
                t1=t1*-1;
                t2=t2*-1;
                t3=t3*-1;
                t4=t4*-1;
                
                //baixa estoque no banco
                expedir(data_saida, ref, cor, t1, t2, t3, t4, n_pedido);
                //alterando situação do pedido para expedido
                
                mudarStatus(n_pedido);
                
        }//fim RowCount 
        
        JOptionPane.showMessageDialog(null, "Pedido Nº "+txt_n_pedido_pesquisar.getText()+" foi expedido com sucesso!(baixa estoque) ");
        DefaultTableModel model =  (DefaultTableModel) jTable1.getModel(); 
        model.setNumRows(0);
        txt_n_pedido_pesquisar.requestFocus();
        txt_n_pedido_pesquisar.setText("");
        txt_status.setText("");
            
        
        
            //desativar botao expedir se ja foi expedido.
        String status = txt_status.getText();
        if (status.equals("EXPEDIDO") ) {
            btn_Expedir.setEnabled(false);
        }
        else{
            btn_Expedir.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_btn_ExpedirActionPerformed
    //Expedir Pedido (Dar baixa em estoque e mudar STATUS do pedido
    public void expedir(String data_saida, String ref, String cor, Integer t1, Integer t2,Integer t3,Integer t4, Integer n_pedido){
        ExpedicaoDTO expedicao = new ExpedicaoDTO();
        
       
        //criar campo data_saida
        
        expedicao.setData_saida(data_saida);
        expedicao.setCodigo(ref);
        expedicao.setCor(cor);
        expedicao.setTotal_t1(t1);
        expedicao.setTotal_t2(t2);
        expedicao.setTotal_t3(t3);
        expedicao.setTotal_t4(t4);
        Integer total = t1+t2+t3+t4;
        expedicao.setTotal_ref(total);
        expedicao.setN_pedido(n_pedido);
        
        
        
            ExpedicaoDAO dao = new ExpedicaoDAO();
            if (dao.baixaEstoque(expedicao)){
                
            }
            else {
                JOptionPane.showMessageDialog(null, "Erro, o pedido de nº: "+txt_n_pedido_pesquisar.getText()+" não foi expedido! ");
            }
        
        
    }
    
    public void mudarStatus(Integer n_pedido){
        
                        ExpedicaoDTO expedicao = new ExpedicaoDTO();
                        
                        ExpedicaoDAO dao = new ExpedicaoDAO();
                        if (dao.mudarStatus(n_pedido)){
                            txt_status.setText("EXPEDIDO");
                            
                            
                        }
                        else {
                            JOptionPane.showMessageDialog(null, "Erro, STATUS NÃO MUDOU!");
                        }
        
    }
    
   

    
