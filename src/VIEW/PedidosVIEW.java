    private void btn_AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarActionPerformed
        // TODO add your handling code here:
        PedidoDTO pedido = new PedidoDTO();

        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha e clique em editar!");
        }
        else{

            pedido.setId(Integer.parseInt(txt_id.getText()));
            pedido.setN_pedido(Integer.parseInt(txt_n_pedido.getText()));
            pedido.setCodigo_cliente(Integer.parseInt(txt_codigo_cliente.getText()));
            pedido.setRazao(txt_Razao.getText());
            pedido.setFantasia(txt_Fantasia.getText());

            pedido.setData_entrega(txt_data_entrega.getText());
            pedido.setPrioriadade(Integer.parseInt(jComboBoxPrioridade.getSelectedItem().toString()));
            pedido.setStatus(jComboBoxStatus.getSelectedItem().toString());

            // fazendo a validação dos dados
            if ((txt_n_pedido.getText().isEmpty()) || (txt_codigo_cliente.getText().isEmpty()) || (txt_data_entrega.getText().isEmpty()) || (txt_Razao.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
            }
            else {
                // instanciando a classe ProdutoDAO do pacote dao e criando seu objeto dao

                PedidoDAO dao = new PedidoDAO();
                if (dao.alterar(pedido)){
                    JOptionPane.showMessageDialog(null, "Pedido "+txt_n_pedido.getText()+" alterado com sucesso! ");

                    // apaga os dados preenchidos nos campos de texto
                    txt_id.setText("");
                    txt_n_pedido.setText("");
                    txt_codigo_cliente.setText("");
                    txt_data_entrega.setText("00/00/0000");
                    txt_Razao.setText("");
                    txt_Fantasia.setText("");
                    jComboBoxPrioridade.setSelectedIndex(0);
                    jComboBoxStatus.setSelectedIndex(0);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Erro, o pedido "+txt_n_pedido.getText()+" não foi alterado!");
                }

            }

        }
    }//GEN-LAST:event_btn_AtualizarActionPerformed

