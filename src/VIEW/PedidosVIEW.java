    private void btn_Cadastrar_PedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Cadastrar_PedidoActionPerformed
        // TODO add your handling code here:
       
        // fazendo a validação dos dados
        if ((txt_n_pedido.getText().isEmpty()) || (txt_codigo_cliente.getText().isEmpty()) || (txt_data_entrega.getText().isEmpty()) || (txt_Razao.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
        }
        else {

        //pegando data atual 
       Date data = new Date(System.currentTimeMillis());    
       SimpleDateFormat formatarDate = new SimpleDateFormat("dd/MM/yyyy");
       
         
        PedidoDTO pedidos = new PedidoDTO();
        pedidos.setData_cadastro(formatarDate.format(data));
        pedidos.setN_pedido(Integer.parseInt(txt_n_pedido.getText()));
        pedidos.setCodigo_cliente(Integer.parseInt(txt_codigo_cliente.getText()));
        pedidos.setData_entrega(txt_data_entrega.getText());
        //prioriadade
        pedidos.setPrioriadade(Integer.parseInt(jComboBoxPrioridade.getSelectedItem().toString()));
        //status pedido
        pedidos.setStatus(jComboBoxStatus.getSelectedItem().toString());
        
        pedidos.setRazao(txt_Razao.getText());
        pedidos.setFantasia(txt_Fantasia.getText());
        
        if (jCheckBox_Especial.isSelected()){
            pedidos.setTipo_pedido("ESPECIAL");
        }else{
            pedidos.setTipo_pedido("DIVERSOS");
        }
         

            // instanciando a classe ProdutoDAO do pacote dao e criando seu objeto dao

            PedidoDAO dao = new PedidoDAO();
            if (dao.adiciona_pedido(pedidos)){
                JOptionPane.showMessageDialog(null, "Pedido "+txt_n_pedido.getText()+" adicionado com sucesso! ");
                    
                
                
                
                DefaultTableModel model =  (DefaultTableModel) jTable1.getModel(); 
                model.setNumRows(0);
                model.addRow(new Object[]{pedidos.getId(), pedidos.getData_cadastro(), new Boolean(false), pedidos.getN_pedido(), pedidos.getCodigo_cliente(), pedidos.getData_entrega(), pedidos.getRazao(), pedidos.getFantasia(), pedidos.getPrioriadade(), pedidos.getStatus(), pedidos.getTipo_pedido()});
                    //listando todos pedidos
                   //listar_pedidos();
                     
                    // apaga os dados preenchidos nos campos de texto
                    txt_n_pedido.setText("");
                    txt_codigo_cliente.setText("");
                    txt_data_entrega.setText("");
                    txt_Razao.setText("");
                    txt_Fantasia.setText("");
                    jComboBoxPrioridade.setSelectedIndex(0);
                    jComboBoxStatus.setSelectedIndex(0);
                    jCheckBox_Especial.setSelected(false);

            }
            else {
                JOptionPane.showMessageDialog(null, "Erro, o pedido "+txt_n_pedido.getText()+" não foi cadastrado! ");
            }
           
        }
        
        btn_Itens_Pedido.requestFocus();
    }//GEN-LAST:event_btn_Cadastrar_PedidoActionPerformed

