    public boolean existe_pedido(String n_pedido){
         
        PedidoDAO dao = new PedidoDAO();
        try {
            if (dao.temPedido(n_pedido)){
                return true;
            }
            else {
                JOptionPane.showMessageDialog(null, "Nº Pedido não existe");
                return false;
            }
        } catch (SQLException ex) {
            System.out.println("Erro ao verificar");
            System.err.println(ex.getMessage());
        }
        
        return false;
    }
    
    public void listar_pedidoX(String n_pedido){
        
        
       PedidoDAO dao = new PedidoDAO();
       DefaultTableModel model =  (DefaultTableModel) jTable1.getModel(); 
            
        model.setNumRows(0);
       
        for (PedidoDTO pedidos : dao.getPedidoX(n_pedido, "TODOS")) {
            
           if (pedidos.getId()!=null){
           
           model.addRow(new Object[]{pedidos.getId(), pedidos.getData_cadastro(),new Boolean(false), pedidos.getN_pedido(), pedidos.getCodigo_cliente(), pedidos.getData_entrega(), pedidos.getRazao(), pedidos.getFantasia(), pedidos.getPrioriadade(), pedidos.getStatus(), pedidos.getTipo_pedido()});
           }
           else{
               
               JOptionPane.showMessageDialog(null, "Pedido não existe.");
           }
           
        }
        //fim listar
    }
    
